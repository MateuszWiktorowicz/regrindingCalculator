CREATE TABLE IF NOT EXISTS tool_type (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS tool (
    id INT AUTO_INCREMENT PRIMARY KEY,
    diameter INT,
    flutes_number INT,
    face_grinding_time_minutes INT,
    periphery_grinding_time_minutes_2D INT,
    id_tool_type INT,
    FOREIGN KEY (id_tool_type) REFERENCES tool_type(id)
);


CREATE TABLE IF NOT EXISTS coating_type (
    id INT AUTO_INCREMENT PRIMARY KEY,
    coating_composition VARCHAR(50),
    mastermet_name VARCHAR(50),
    mastermet_code VARCHAR(10)
);


CREATE TABLE IF NOT EXISTS coating_price (
    id INT AUTO_INCREMENT PRIMARY KEY,
    id_coating INT,
    diameter INT,
    price FLOAT,
    FOREIGN KEY (id_coating) REFERENCES coating_type(id)
);

